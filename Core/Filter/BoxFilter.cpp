#pragma once

#include "Filter/BoxFilter.h"

using namespace Illumina::Core;

//----------------------------------------------------------------------------------------------
void BoxFilter::operator()(Vector2 *p_pSamples, int p_nSampleCount)
{
	for (int i = 0; i < p_nSampleCount; i++)
	{
		p_pSamples[i].Set(
			p_pSamples[i].X - 0.5f,
			p_pSamples[i].Y - 0.5f);
	}
}
